from PIL import Image
def prev(text, scale=6):
    im=Image.new('RGB',(len(text)*8*scale,8*scale),(0,0,40)); px=im.load()
    for i,c in enumerate(text):
        g=G.get(c,[0]*8)
        for y in range(8):
            for x in range(8):
                if g[y]&(0x80>>x):
                    for sy in range(scale):
                        for sx in range(scale):
                            px[(i*8+x)*scale+sx,y*scale+sy]=(255,255,255)
    return im
lines=["Arcade Volleyball","PL1 Keyboard  0","Sound On  Exit","0123456789 :.,!?"]
ims=[prev(l) for l in lines]
W=max(i.width for i in ims); H=sum(i.height+4 for i in ims)
M=Image.new('RGB',(W,H),(0,0,40)); y=0
for i in ims: M.paste(i,(0,y)); y+=i.height+4
